import {
    IsEnum,
    IsInt,
    IsOptional,
    IsString,
    Max,
    Min,
} from 'class-validator'
import { Type } from 'class-transformer'
import { MessageStatus } from '@prisma/client'

export class ListContactMessagesDto {
    @IsOptional()
    @IsEnum(MessageStatus)
    status?: MessageStatus

    @IsOptional()
    @IsString()
    search?: string

    @IsOptional()
    @Type(() => Number)
    @IsInt()
    @Min(1)
    page?: number = 1

    @IsOptional()
    @Type(() => Number)
    @IsInt()
    @Min(1)
    @Max(100)
    limit?: number = 20
}
